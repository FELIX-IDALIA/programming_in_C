#include <stdio.h>
/**
 * main - starting point.
 *
 * Return: 0 (success).
 *
 */
int main(void)
{
	/**
	 * To print different types in a single printf() function,
	 * you can use the following
	 */
	int age = 18;
	int year = 4;
	char grade = 'A';

	printf("Age = %d.\nYear = %dth.\nGrade = %c.", age, year, grade);
	return (0);
}
